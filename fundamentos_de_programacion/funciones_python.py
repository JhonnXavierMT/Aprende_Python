
#¿Qué es una función?  
#🗣️:  
#"Una función es como una receta. 📖🍰  
#Tiene pasos que sigues para lograr algo. En Python, sirve para organizar tu código y evitar repetir instrucciones."


def saludar():  
    print("¡Hola, mundo!")  

"Este es un ejemplo de función. **Def** es para 'definir', le damos un nombre y luego escribimos qué hará."


"Fácil, solo tienes que llamarla por su nombre. Mira esto:"

saludar()  

"¡Y listo! Ejecuta el bloque de código definido. 📤"


#'Funciones con parámetros']
#🗣️:
"Ahora, si quieres que tu función sea más útil, puedes agregarle parámetros 🍳"
  
def saludar(nombre):  
    print(f"¡Hola,!",nombre)  
  
#🗣️:  
"Cuando llamas a la función, le das un valor. Mira:"

saludar("Cristiano Ronaldo")  

#'Funciones con retorno']**  
 
def sumar(a, b):  
    return a + b  
resultado = sumar(5, 5)  
print(resultado)  # Salida: 10 

