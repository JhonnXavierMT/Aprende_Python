"""Leer un número y determinar si 
   este es un número par o impar"""
#Leer numero
numero = int(input("ingrese un numero:\n"))
#Condicion que verifica si es par
if numero % 2==0:
    #mensaje si es Par
    print(f"{numero} es un numero Par")
else:
    #mensaje si es Impar
    print(f"{numero} es un numero Impar")